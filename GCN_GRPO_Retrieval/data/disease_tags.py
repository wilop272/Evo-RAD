def get_disease_tags(disease_name: str) -> List[str]:
    """Return the curated tag list for a disease (empty list if missing)."""
    tags = DISEASE_CLINICAL_TAGS.get(disease_name)
    if not tags:
        # Fallback to lowercase
        tags = DISEASE_CLINICAL_TAGS.get(disease_name.lower())
    return tags if tags else []


def get_all_unique_tags() -> Set[str]:
    """Return the union of all unique tags."""
    unique: Set[str] = set()
    for tags in DISEASE_CLINICAL_TAGS.values():
        unique.update(tags)
    return unique
